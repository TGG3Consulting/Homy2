/**
 * Property chat context prompt template
 */
export function buildPropertyChatPrompt(
  property: {
    address: string;
    district: string;
    price: number;
    currency: string;
    area?: number;
    sizeSqm?: number;
    rooms: number;
    floor?: number;
    totalFloors?: number;
    yearBuilt?: number;
    buildingType?: string;
    condition?: string;
    hasParking?: boolean;
    hasBalcony?: boolean;
    petsAllowed?: boolean;
    depositMonths?: number;
    utilitiesEstimate?: number;
  },
  conversationHistory: string,
  chatHistory: string,
  userMessage: string
): string {
  return `Ты AI-консультант Homy. Клиент сейчас смотрит КОНКРЕТНЫЙ объект и задаёт вопрос о НЁМ.

===== КРАТКАЯ ИСТОРИЯ ПОИСКА КЛИЕНТА =====
${conversationHistory || 'Нет данных'}
==========================================

===== ОБЪЕКТ КОТОРЫЙ КЛИЕНТ СЕЙЧАС СМОТРИТ =====
Адрес: ${property.address}, ${property.district}
Цена: ${property.price} ${property.currency}/мес
Площадь: ${property.area || property.sizeSqm}м²
Комнаты: ${property.rooms}
Этаж: ${property.floor || '?'}/${property.totalFloors || '?'}
Год постройки: ${property.yearBuilt || 'не указан'}
Тип здания: ${property.buildingType || 'не указан'}
Состояние: ${property.condition || 'не указано'}
Парковка: ${property.hasParking ? 'есть' : 'нет'}
Балкон: ${property.hasBalcony ? 'есть' : 'нет'}
Животные: ${property.petsAllowed ? 'можно' : 'нельзя'}
Депозит: ${property.depositMonths || 1} мес
Коммуналка: ~${property.utilitiesEstimate || 20000} AMD/мес
================================================

===== ИСТОРИЯ ЧАТА В ЭТОЙ КАРТОЧКЕ =====
${chatHistory || 'Начало разговора'}
========================================

ВОПРОС КЛИЕНТА: ${userMessage}

Отвечай кратко (2-4 предложения), по делу, на языке вопроса. Ты знаешь ВСЁ об этом объекте.`;
}

